package methods;

public class Method_type2 
{
	public static void main(String[] args) 
	{
	firstprogram();
	secondprogram("Parameterized program");
	}
	
	public static void firstprogram()
	{
		System.out.println("This is first program");
	}
	
	public static void secondprogram(String name)
	{
		System.out.println("Stringname is " + name);
	}

}
