package abstraction_concepts;

public abstract class Abstract_3 
{
	
	// AN ABSTRACT CLASS WITH NO ABSTRACT METHOD BUT STILL WORKING MEANS 
	// AN ABSTRACT CLASS WORKS WITHOUT ANY ABSTRACT METHOD IN IT.
	
	public void breath()
	{
		System.out.println("Breath Function");
	}
	
	public float add(int number_1, float number_2)
	{
		return number_1 + number_2;
	}
}
