package for_loops;
// simple for loop with independent criteria.
public class For_1 {

	public static void main(String[] args) 
	{
		int number = 0;
		
		for(System.out.println("Hello");number <= 10;System.out.println("Hi...."))
		{
			number++;
			System.out.println("Inside for loop");
		}

	}

}
